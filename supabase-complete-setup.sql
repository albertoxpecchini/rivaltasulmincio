create or replace function public.contains_blocked_words(input_text text)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.blocked_words b
    where lower(coalesce(input_text, '')) ~ ('(^|\\W)' || regexp_replace(lower(b.word), '([\\W])', '\\\\1', 'g') || '(\\W|$)')
  );
$$;

